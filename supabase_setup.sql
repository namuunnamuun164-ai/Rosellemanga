drop function if exists public.increment_manga_views(bigint);
-- ЗАСВАР #190 (код шинжилгээ): өмнө нь void буцаадаг байсан тул клиент тал
-- (App.jsx) сервер бодитоор шинэ vзэлт тоолсон эсэхийг мэдэхгvйгээр vргэлж
