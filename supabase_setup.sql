-- клиент зөвхөн ЖИНХЭНЭ шинэ тооллого vед л (true vед) +1 нэмнэ.
drop function if exists public.increment_manga_views(bigint, text);
create or replace function public.increment_manga_views(input_id bigint, viewer_key text default null)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  effective_key text;
  recent_count int;
begin
  -- ЗАСВАР #163 (код шинжилгээ): хорлонтой клиент хэт урт viewer_key дамжуулж
  -- хvснэгт/индексийг бөглөхөөс сэргийлж 64 тэмдэгтээр таслав.
  effective_key := coalesce(auth.uid()::text, left(nullif(trim(viewer_key), ''), 64));
  if effective_key is null then
    return false;
  end if;

  select count(*) into recent_count
  from public.manga_view_events e
  where e.manga_id = input_id
    and e.viewer_key = effective_key
    and e.viewed_at > now() - interval '30 minutes';

  if recent_count > 0 then
    return false;
  end if;

  update public.mangas set views = views + 1 where id = input_id;
  insert into public.manga_view_events (manga_id, viewer_key) values (input_id, effective_key);
  return true;
end;
$$;

grant execute on function public.increment_manga_views(bigint, text) to anon, authenticated;

-- Сүүлийн N хоногт хамгийн их үзэгдсэн K мангаг буцаана
create or replace function public.top_manga_last_days(days_back int default 30, result_limit int default 10)
returns table(manga_id bigint, recent_views bigint)
language sql
stable
security definer
set search_path = public
as $$
  select manga_id, count(*) as recent_views
  from public.manga_view_events
  where viewed_at > now() - (days_back || ' days')::interval
  group by manga_id
  order by recent_views desc
  limit result_limit;
$$;

grant execute on function public.top_manga_last_days(int, int) to anon, authenticated;

-- ЗАСВАР #163: admin-ий "📊 СТАТИСТИК" таб — өдрийн аль цагт хамгийн их
-- уншигддагийг харуулна. Зөвхөн admin дуудаж болно (function дотроо шалгана).
create or replace function public.admin_views_by_hour(days_back int default 30)
returns table(hour_of_day int, view_count bigint)
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.has_any_role(auth.uid(), array['admin']) then
    raise exception 'Зөвхөн admin энэ vйлдлийг хийж болно.';
  end if;
  return query
    select extract(hour from (e.viewed_at at time zone 'Asia/Ulaanbaatar'))::int as hour_of_day,
           count(*) as view_count
    from public.manga_view_events e
    where e.viewed_at > now() - (days_back || ' days')::interval
    group by 1
    order by 1;
end;
$$;

grant execute on function public.admin_views_by_hour(int) to authenticated;

